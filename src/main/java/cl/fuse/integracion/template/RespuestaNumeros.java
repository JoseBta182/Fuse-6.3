package cl.fuse.integracion.template;

public class RespuestaNumeros {
	
	private String numMayor;
	private String numMenor;
	private String numLista;
	
	
	public String getNumMayor() {
		return numMayor;
	}
	public void setNumMayor(String numMayor) {
		this.numMayor = numMayor;
	}
	public String getNumMenor() {
		return numMenor;
	}
	public void setNumMenor(String numMenor) {
		this.numMenor = numMenor;
	}
	public String getNumLista() {
		return numLista;
	}
	public void setNumLista(String numLista) {
		this.numLista = numLista;
	}
	

}
